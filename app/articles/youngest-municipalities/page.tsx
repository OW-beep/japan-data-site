import { getMunicipalities } from "@/lib/municipalities";
import ArticleLayout from "@/components/ArticleLayout";

export const metadata = {
  title: "若い自治体ランキング｜全国自治体データ",
  description:
    "高齢化率が低く、若い世代の割合が高い自治体を紹介します。都心のタワーマンション立地から震災復興地域まで、その背景を解説します。",
};

export default function Page() {
  const ranking = getMunicipalities()
    .filter((c) => c.elderlyPopulation && c.population)
    .map((c) => ({
      ...c,
      rate: (c.elderlyPopulation / c.population) * 100,
    }))
    .sort((a, b) => a.rate - b.rate)
    .slice(0, 30);

  const average =
    ranking.reduce((s, c) => s + c.rate, 0) /
    ranking.length;

  return (
    <ArticleLayout
      title="若い自治体ランキング"
      summary="高齢化率が低い(＝若い世代の割合が高い)自治体を集めました。都心のオフィス街から、震災からの復興が進む地域まで、その顔ぶれは一様ではありません。"
      heroLabel="TOP30平均高齢化率"
      heroValue={`${average.toFixed(1)}%`}
      rankingLink="/ranking/aging"
      top3={[
        {
          rank: 1,
          name: ranking[0].name,
          value: `高齢化率${ranking[0].rate.toFixed(1)}%`,
        },
        {
          rank: 2,
          name: ranking[1].name,
          value: `高齢化率${ranking[1].rate.toFixed(1)}%`,
        },
        {
          rank: 3,
          name: ranking[2].name,
          value: `高齢化率${ranking[2].rate.toFixed(1)}%`,
        },
      ]}
    >
      <div style={box}>
        <h2>高齢化率が低い自治体TOP15</h2>

        <p style={{ marginBottom: 16, color: "#4b5563" }}>
          日本全体の高齢化率が約29%であることを踏まえると、
          ここに挙がる自治体はいずれも全国平均を大きく
          下回る、極めて特異な人口構成を持っています。
          ただし後述の通り、その理由は自治体によって
          まったく異なります。
        </p>

        <ol>
          {ranking.slice(0, 15).map((c) => (
            <li key={c.code}>
              {c.name}（高齢化率{c.rate.toFixed(1)}%、人口
              {c.population.toLocaleString()}人）
            </li>
          ))}
        </ol>
      </div>

      <div style={box}>
        <h2>分析：3つの異なるタイプの「若さ」</h2>

        <p>
          高齢化率が最も低いのは{ranking[0].name}
          （{ranking[0].rate.toFixed(1)}%）です。これは
          福島県の帰還困難区域の一部を含む町であり、
          2011年の原発事故による長期避難ののち、
          除染やインフラ復旧が進んだ地域から順次
          避難指示が解除され、主に復興関連の作業員や
          帰還を決めた現役世代が居住していることが、
          見かけ上の高齢化率の低さにつながっています。
          震災前の人口構成とは大きく異なる、特殊な
          状況にある点に留意が必要です。
        </p>

        <p>
          一方、東京都中央区・千代田区・港区・渋谷区・
          文京区といった東京都心部の特別区も上位に
          並んでいます。これらの地域では、2000年代以降の
          タワーマンション開発により、子育て世代や
          若い単身世帯・共働き世帯(いわゆるDINKs)の
          転入が続いており、高齢化率を押し下げる
          要因になっています。ただし、これは必ずしも
          「子どもが多い」ことを意味するわけではなく、
          子ども比率ランキングで見ると、都心3区は
          必ずしも上位には入りません。高齢者が少ない
          ことと、子どもが多いことは、似ているようで
          異なる現象です。
        </p>

        <p>
          3つ目のタイプが、愛知県長久手市・埼玉県戸田市・
          千葉県浦安市のような、大都市近郊のニュータウン・
          ベッドタウンです。長久手市は名古屋市に隣接し、
          大学や研究機関の立地、計画的な住宅開発が
          進んだことで若い世代の転入が続いています。
          浦安市も東京ディズニーリゾートの開業以降、
          再開発によるマンション供給が進み、子育て世代の
          流入が続いてきた代表例です。こうした自治体は、
          高齢化率の低さと子ども比率の高さが同時に
          見られる、最も「若い街」らしい特徴を持って
          います。
        </p>

        <p>
          このように、高齢化率の低さという同じ結果でも、
          その背景は「震災復興」「都心居住」
          「計画的なニュータウン開発」と大きく異なります。
          ランキングの数字だけでなく、その自治体が
          どのような経緯でその数字に至ったのかを知ることで、
          データの見え方はより立体的になります。
        </p>
      </div>

      <div style={box}>
        <h2>「若い自治体」の今後</h2>

        <p>
          都心のタワーマンションやニュータウンによって
          支えられている「若さ」は、開発が一巡した後、
          そのまま同じ世代が一斉に高齢化していくという
          長期的な課題も抱えています。実際、1960〜70年代に
          開発された郊外のニュータウンの多くは、当時
          入居した世代がそろって高齢化し、現在は逆に
          高齢化率が高い地域に変わっている例が全国に
          数多く見られます。今回ランキングに挙がった
          自治体が、将来にわたって「若い自治体」であり
          続けられるかどうかは、継続的に新しい世代の
          転入を呼び込めるかどうかにかかっています。
          再開発から数十年が経過した団地・ニュータウンで
          高齢化率が急上昇するケースは、今後の都市計画に
          おいても重要な教訓となっています。
        </p>

        <p>
          本サイトでは、高齢化率ランキング・子ども人口
          割合ランキングを定期的に更新しています。
          同じ自治体が数年後にどう変化しているかを
          追跡することで、こうした「若さ」の持続性を
          確認することもできます。
        </p>
      </div>

      <div style={box}>
        <h2>「若い自治体」と「子どもが多い自治体」は違う</h2>

        <p>
          今回のランキングは高齢化率の低さを基準に
          並べたものですが、これは必ずしも子ども人口
          割合ランキングと同じ顔ぶれにはなりません。
          たとえば東京都心の特別区は、高齢化率こそ
          低いものの、子ども人口割合で見ると全国トップ
          クラスにはあまり入ってきません。単身世帯や
          共働きで子どものいない世帯(DINKs)の割合が
          高いことが理由と考えられます。逆に、沖縄県の
          町村や大都市近郊のニュータウンは、高齢化率の
          低さと子ども人口割合の高さの両方を兼ね備えて
          いる傾向があります。
        </p>

        <p>
          自治体の「若さ」を正しく理解するには、
          高齢化率だけでなく、子ども人口割合もあわせて
          確認することが欠かせません。本サイトの
          子ども人口割合ランキングとあわせてご覧いただくと、
          それぞれの自治体がどのようなタイプの「若さ」を
          持っているのかが、より具体的に見えてくるはずです。
        </p>
      </div>

      <div style={box}>
        <h2>まとめ</h2>

        <p>
          高齢化率が低い自治体には、震災復興地域、
          都心のタワーマンション立地、計画的な
          ニュータウン開発という、まったく異なる
          3つの背景がありました。同じ「若い自治体」
          という結果でも、その持続性やなりたちは
          自治体ごとに大きく異なります。ランキングの
          順位だけを見るのではなく、その土地の歴史や
          都市計画の経緯まで含めて理解することで、
          データはより深い意味を持つようになります。
        </p>
      </div>
    </ArticleLayout>
  );
}

const box: React.CSSProperties = {
  background: "#fff",
  padding: 16,
  borderRadius: 12,
  border: "1px solid #e5e7eb",
  marginBottom: 20,
};
